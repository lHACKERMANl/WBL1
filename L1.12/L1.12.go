package main

import (
	"fmt"
)

/*
Для создания собственного множества строк (набора уникальных строк) из заданной последовательности строк,
будем использовать карту (map), где ключи будут представлять уникальные строки.
*/

func main() {
	// Заданная последовательность строк
	strings := []string{"cat", "cat", "dog", "cat", "tree"}

	// Создаем карту для хранения уникальных строк (множества)
	stringSet := make(map[string]bool)

	// Добавляем строки в множество, удаляя дубликаты
	for _, str := range strings {
		stringSet[str] = true
	}

	// Выводим уникальные строки (множество)
	fmt.Println("Собственное множество строк:")
	for str := range stringSet {
		fmt.Println(str)
	}
}
